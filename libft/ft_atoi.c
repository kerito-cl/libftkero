#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	hold;
	int	k;

	i = 0;
	n = 1;
	hold = 0;
	k = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		k = hold * 10;
		hold = (str[i] - '0') + k;
		i++;
	}
	return (hold * n);
}
